#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*l;

	while (*lst)
	{
		l = *lst;
		*lst = (*lst)->next;
		ft_lstdelone(l, del);
	}
}
